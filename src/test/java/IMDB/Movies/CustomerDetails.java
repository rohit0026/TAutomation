package IMDB.Movies;

public class CustomerDetails {

	private int ID;
	private String Salary;
	private String Name;

	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getSalary() {
		return Salary;
	}
	public void setSalary(String salary) {
		Salary = salary;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		this.Name = name;
	}



}
